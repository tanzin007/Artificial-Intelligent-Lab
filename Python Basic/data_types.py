# In Python, numeric data type represents the data that has a numeric value. 

# A numeric value can be integer, floating number, or even complex number. 

# These values are defined as int, float, and complex classes in Python.


# Integers - This value is represented by int

# Float - This value is represented by the float

# Complex Numbers - Complex number is represented by a complex class. It is specified as (real part) + (imaginary part)j. For example - 2+3j



# Python program to 
# demonstrate numeric value

a = 5
print("Type of a: ", type(a))

